my_string = "Hello World"

# get the length of the string
print(len(my_string))

# indexing example
print(my_string[0])

# negetive indexing will also works and it starts from -1 i.e last character of the string
print(my_string[-1])

# reverse a string with negetive indexing
print(my_string[::-1])

# get the type using type function
print(type(my_string))